

namespace DeepSigma.AutoML.Enums;

/// <summary>
/// AIIntelligenceCapacity specifies the levels of artificial intelligence capabilities, including Strong AI (Artificial General Intelligence) and Weak AI (Narrow AI), which represent different stages of AI development and functionality.
/// Strong AI possessing human-like intelligence and the ability to understand and learn across a wide range of tasks, while Weak AI is designed for specific tasks without general intelligence or learning capabilities.
/// </summary>
public enum AIIntelligenceCapacity
{
    /// <summary>
    /// Strong AI, also known as Artificial General Intelligence (AGI), refers to a type of artificial intelligence that possesses the ability to understand, learn, and apply knowledge across a wide range of tasks and domains, similar to human intelligence.
    /// </summary>
    Strong,
    /// <summary>
    /// Weak AI, also known as Narrow AI, refers to a type of artificial intelligence that is designed and trained to perform specific tasks or functions, without possessing general intelligence or the ability to understand and learn across a wide range of tasks and domains like human intelligence.
    /// </summary>
    Weak,
}
