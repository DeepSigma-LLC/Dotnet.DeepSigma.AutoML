/// <summary>
/// Specifies whether a statistical distribution is continuous or discrete.
/// </summary>
public enum StatisticalDistributionType
{
    /// <summary>
    /// Continuous distribution represents a type of probability distribution where the random variable can take on any value within a specified range or interval.
    /// </summary>
    Continuous,
    /// <summary>
    /// Discrete distribution represents a type of probability distribution where the random variable can only take on specific, distinct values, often whole numbers.
    /// </summary>
    Discrete
}
