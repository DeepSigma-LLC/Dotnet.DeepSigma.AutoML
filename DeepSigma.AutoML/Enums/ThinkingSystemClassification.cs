

namespace DeepSigma.AutoML.Enums;

/// <summary>
/// The ThinkingSystemClassification enumeration defines different classifications of thinking systems, which are cognitive processes that can be categorized based on their characteristics and functions.
/// </summary>
public enum ThinkingSystemClassification
{
    /// <summary>
    /// SystemOneThinking, also known as intuitive or automatic thinking, is a type of cognitive processing that is fast, effortless, and often operates below the level of conscious awareness.
    /// System 1 thinking represents an automatic, intuitive, and near-instantaneous process. 
    /// It shares similarities with generative models that automatically generate tokens without any self-reflective behavior
    /// </summary>
    SystemOneThinking,
    /// <summary>
    /// SystemTwoThinking, also known as analytical or deliberate thinking, is a type of cognitive processing that is slow, effortful, and requires conscious attention and reasoning.
    /// System 2 thinking is a conscious, slow, and logical process, akin to brainstorming and self-reflection. 
    /// System 2 thinking tends to produce more thoughtful responses than system 1 thinking
    /// </summary>
    SystemTwoThinking
}
