namespace DeepSigma.AutoML.Enums.Modeling;

/// <summary>
/// The common saying is: "Complexity either exists in your code or in your user interface". 
/// It is often used in the context of software and product development to describe the trade-off between making a system easy for developers to build versus easy for users to operate. 
/// However, the same concept can be applied to many other areas, such as system design, architecture, and even organizational structure.
/// </summary>
public enum SystemComplexityTradeoffType
{
    /// <summary>
    /// SystemComplexity indicates that the complexity is primarily in the system / model itself, such as in its architecture, design, or implementation.
    /// </summary>
    SystemComplexity,
    /// <summary>
    /// InterfaceComplexity indicates that the complexity is primarily in the user interface, such as in its usability, navigation, or overall user experience.
    /// </summary>
    InterfaceComplexity
}
