
namespace DeepSigma.AutoML.Enums.Statistical;

/// <summary>
/// Specifies the type of significance in statistical analysis.
/// </summary>
public enum SignificanceType
{
    /// <summary>
    /// Indicates that the results are statistically significant, meaning they are unlikely to have occurred by chance.
    /// </summary>
    StatisticallySignificant,
    /// <summary>
    /// Indicates that the results are practically significant, meaning they have real-world relevance or importance, even if they may not be statistically significant.
    /// </summary>
    PracticallySignificant
}
