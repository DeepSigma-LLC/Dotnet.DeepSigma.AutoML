
namespace DeepSigma.AutoML.Enums.Statistical;

/// <summary>
/// Specifies whether a data set represents a population or a sample.
/// </summary>
public enum DataSetCategorization
{
    /// <summary>
    /// Indicates that the data set represents an entire population.
    /// </summary>
    Population,
    /// <summary>
    /// Indicates that the data set represents a sample drawn from a population.
    /// </summary>
    Sample
}
