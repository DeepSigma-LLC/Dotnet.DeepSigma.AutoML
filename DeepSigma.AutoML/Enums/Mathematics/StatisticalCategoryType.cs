
namespace DeepSigma.AutoML.Enums.Mathematics;

/// <summary>
/// Specifies the category of statistics being referred to.
/// </summary>
public enum StatisticalCategoryType
{
    /// <summary>
    /// Descriptive statistics involves summarizing and organizing data so that it can be easily understood.
    /// </summary>
    Descriptive,
    /// <summary>
    /// Inferential statistics involves using a random sample of data taken from a population to describe and make inferences about the population.
    /// </summary>
    Inferential,
    /// <summary>
    /// Predictive statistics involves using historical data and statistical models to make predictions about future events or outcomes.
    /// Also known as Predictive Analytics.
    /// </summary>
    Prediction
}
